x = int(input("Enter a Grade : "))

if x >= 80 :
    print("Grade A")
elif x >= 75 :
    print("Grade B+")
elif x >= 70 :
    print("Grade B")
elif x >= 65 :
    print("Grade C+")
elif x >= 60 :
    print("Grade C")
elif x >= 55 :
    print("Grade D+")
elif x >= 50 :
    print("Grade D")
else :    
    print("Grade F")

print()